const { obtenerCalificacionesCI, obtenerCalificacionesCA, obtenerCalificacionesRF } = require('../models/calificaciones_d_2');

const obtenerCalificacionesJSONCI = (req, res) => {
  const id_usuario = req.session.id_usuario;  // <-- aquí usas id_usuario como en sesión

  if (!id_usuario) {
    return res.status(401).json({ error: 'No autorizado, no hay sesión válida' });
  }

  obtenerCalificacionesCI(id_usuario, (err, calificaciones) => {
    if (err) {
      return res.status(500).json({ error: 'Error al obtener datos' });
    }
    res.json(calificaciones);
  });
};

